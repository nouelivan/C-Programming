// This file contains the function definitions.

#include <iostream>
#include <vector>

// This function greets the player upon starting the game.
void greet() {

  std::cout << "=============\n";
  std::cout << "UFO: The Game\n";
  std::cout << "=============\n";
  std::cout << "Instructions: save your friend from alien abduction by guessing the letters in the codeword.\n";

}

// This function displays a message at the end of the game.
void end_game(std::string answer, std::string codeword) {

  if (answer == codeword) {
    std::cout << "\nHooray! You saved the person and earned a medal of honor!\n";
  } else {
    std::cout << "\nOh no! The UFO just flew away with another person!\n";
  }

}

// This function displays the status of the game.
void display_status(std::vector<char> incorrect, std::string answer) {

  std::cout << "\nIncorrect Guesses:\n";
  for (char c: incorrect) {
    std::cout << c << " "; 
  }
  std::cout << "\nCodeword:\n";
  for (int i = 0; i < answer.length(); i++) {
    std::cout << answer[i] << " ";
  }
  
}


